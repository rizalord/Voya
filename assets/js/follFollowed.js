let folBot = document.querySelector('#follow');
let followedBot = document.querySelector('#followed');

folBot.addEventListener('click', function () {
	folBot.classList.toggle('d-none');
	followedBot.classList.toggle('d-none');
});

followedBot.addEventListener('click', function () {
	folBot.classList.toggle('d-none');
	followedBot.classList.toggle('d-none');
});